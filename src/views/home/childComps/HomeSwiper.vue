<template>
  <div id="home-swiper">
    <swiper>
      <swiper-item v-for="(item, index) in banners" :key="index">
        <a :href="item.link">
          <img :src="item.image" @load="imgLoaded" alt="" />
        </a>
      </swiper-item>
    </swiper>
  </div>
</template>

<script>
import { Swiper, SwiperItem } from "components/common/swiper";
export default {
  name: "HomeSwiper",
  props: {
    banners: {
      type: Array,
      default() {
        return [];
      },
    },
  },
  data() {
    return {
      isLoaded: false
    }
  },
  methods: {
    imgLoaded() {
      if(!this.isLoaded) {
        this.$emit("imgLoaded");
        this.isLoaded = true;
      }
    }
  },
  components: {
    Swiper,
    SwiperItem,
  },
};
</script>

